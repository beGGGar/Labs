%график распределения Максвеллла для водорода
clear; clc; cla;
T1=100;
T2=300;
Na=6.022*10^23;
k=1.381*10^(-23);
R=Na*k;
n=0.002;
v = 0:6000;
f1=4*pi*(n/2/pi/R/T1)^(3/2)*exp(-v.^2*n/2/T1/R).*v.^2;
f2=4*pi*(n/2/pi/R/T2)^(3/2)*exp(-v.^2*n/2/T2/R).*v.^2;
plot(v, f1);
grid on; hold on;
plot(v, f2);
xlabel('v'); ylabel('f(v)');
title('График распределения Максвелла по скоростям для водорода');
legend('для Т=100К', 'для Т=300К');
 